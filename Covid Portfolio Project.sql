-- Whole table

SELECT *
FROM PortfolioProject..CovidDeaths



-- Select Data to start with

Select Location, date, total_cases, new_cases, total_deaths, population
From PortfolioProject..CovidDeaths
Where continent is not null 
order by 1,2



-- Total Cases vs Total Deaths
-- Shows likelihood of dying if you contract covid in China

Select Location, date, total_cases, total_deaths, 
	Round((Convert(float, "total_deaths")/Convert(float, "total_cases"))*100, 3) as DeathPercentage
From PortfolioProject..CovidDeaths
Where location like '%China%'
and continent is not null 
order by 1,2



-- Total Cases vs Population
-- Shows what percentage of population infected with Covid

Select Location, date, Population, total_cases,  (total_cases/population)*100 as PercentPopulationInfected
From PortfolioProject..CovidDeaths
--Where location like '%states%'
order by 1,2



-- Countries with Highest Infection Rate compared to Population

Select Location, Population, MAX(CONVERT(int, total_cases)) as HighestInfectionCount,  Max((total_cases/population))*100 as PercentPopulationInfected
From PortfolioProject..CovidDeaths
--Where location like '%states%'
Group by Location, Population
order by PercentPopulationInfected desc



-- Countries with Highest Death Count per Population

Select Location, MAX(cast(Total_deaths as int)) as TotalDeathCount
From PortfolioProject..CovidDeaths
Where continent is not null 
Group by Location
order by TotalDeathCount desc



-- BREAKING THINGS DOWN BY CONTINENT

-- Showing contintents with the highest death count per population

Select Location, MAX(cast(Total_deaths as int)) as TotalDeathCount
From PortfolioProject..CovidDeaths
Where continent is null AND 
	(Location = 'Europe' 
	or Location = 'Asia' 
	or Location = 'North America'
	or Location like 'South America'
	or Location like 'Africa'
	or Location like 'Oceania')
Group by Location
order by TotalDeathCount desc



